''' Faça um programa que leia um número e mostre seu dobro, triplo e a raiz quadrada'''
print('=+=' *20)
num = float(input('Digite um numero:'))
print('o Numero {} tem:'.format(num))
print('{} Representa o Dobro'.format(num*2))
